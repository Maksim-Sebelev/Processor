#ifndef _TOKENIZER_HPP_
#define _TOKENIZER_HPP_

//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

#include "global/global_include.hpp"

//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

enum class Bracket : char
{
    undef_bracket = 0  ,
    left_bracket  = '[',
    right_bracket = ']',
};

//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

enum class Separator : char
{
    undef_separator = 0  ,
    comma           = ',',
    colon           = ':',
};

//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

enum class MathOperator : char
{
    undef_operator = 0  ,
    plus           = '+',
};

//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

using Number = const char*;

//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

enum class TokenType
{
    token_command       ,
    token_register      ,
    token_number        ,
    token_bracket       ,
    token_separator     ,
    token_math_operation,
};

//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

union TokenValue
{
    Cmd          command  ;
    Registers    reg      ;
    Number       number   ;
    Bracket      bracket  ;
    Separator    seprator ;
    MathOperator operation;
};

//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

struct Token
{
    TokenType  type;
    TokenValue value;
};

//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

struct TokensArray
{
    Token* array;
    size_t size;
};

//-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------


#endif // _TOKENIZER_HPP_
