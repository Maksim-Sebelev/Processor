#include <assert.h>
#include "signature/signature.hpp"

//------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

Signature SignatureCtor(const char* standart, const char* autor, const char* version)
{
    assert(standart);
    assert(autor);
    assert(version);

    return (Signature)
    {
        .standart = standart,
        .autor    = autor   ,
        .version  = version ,
    };
}

